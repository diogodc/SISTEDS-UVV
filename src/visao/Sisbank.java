    public void inserir() {
        peb.setNomeCliente(jTextField_Cliente.getText());
        peb.setNomeBanco(jComboBox_Banco.getSelectedItem().toString());
        peb.setAgencia(jTextField_Agencia.getText());
        peb.setNumPoup(Integer.parseInt((String) jTextField_NP.getText()));
        peb.setSaldo(Float.parseFloat((String) jTextField_Saldo.getText()));
        peb.setJurosMensal(Float.parseFloat((String) jTextField_JM.getText()));
        peb.setTaxaAdm(Float.parseFloat((String) jTextField_TA.getText()));
    }
    public void limpar() {
        jTextField_Cliente.setText(null);
        jComboBox_Banco.setSelectedIndex(0);
        jTextField_Agencia.setText(null);
        jTextField_NP.setText(null);
        jTextField_Saldo.setText(null);        
        jTextField_JM.setText(null);
        jTextField_TA.setText(null);

        peb.setId(null);
        peb.setNomeCliente(null);
        peb.setNomeBanco(null);
        peb.setAgencia(null);
        peb.setNumPoup(null);
        peb.setSaldo(0);
        peb.setJurosMensal(0);
        peb.setTaxaAdm(0);
    }

