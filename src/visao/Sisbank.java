    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        if(evt.getClickCount()==2){
            try {    
                bt_addPEB.setEnabled(false);
                bt_editPEB.setEnabled(true);
                bt_excluirPEB.setEnabled(true);
                bt_attPEB.setEnabled(true);
                bt_movPEB.setEnabled(true);

                limpar();                
                
                jTextField_Cliente.setText((String) jTable.getValueAt(jTable.getSelectedRow(), 1));
                jComboBox_Banco.setSelectedItem((String) jTable.getValueAt(jTable.getSelectedRow(), 2));
                jTextField_Agencia.setText((String) jTable.getValueAt(jTable.getSelectedRow(), 3));
                jTextField_NP.setText(Integer.toString((Integer) jTable.getValueAt(jTable.getSelectedRow(), 4)));
                jTextField_Saldo.setText(Float.toString((Float) jTable.getValueAt(jTable.getSelectedRow(), 5)));       
                jTextField_JM.setText(Float.toString((Float) jTable.getValueAt(jTable.getSelectedRow(), 6)));
                jTextField_TA.setText(Float.toString((Float) jTable.getValueAt(jTable.getSelectedRow(), 7)));
                
                peb.setId((int) jTable.getValueAt(jTable.getSelectedRow(), 0));
                
                inserir();
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "ERRO AO RECUPERAR DADOS DA TABELA", "SISTEDS", JOptionPane.ERROR_MESSAGE);
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_jTableMouseClicked

